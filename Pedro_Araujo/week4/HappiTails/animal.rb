
class Animal
  def initialize(name,age,gender,species,toys)
    @name = name
    @age = age
    @gender = gender
    @species = species
    @toys = toys
    @owner = ''
  end
  def show_name
    @name
  end
  def show_age
    @age
  end
  def show_species
    @species
  end
  def show_toys
    @toys
  end
  def show_owner
    @owner
  end
  def change_owner(owner)
    @owner = owner
  end
end



